# （draw_segments と build は前回のままでOK）

def build(font, cfg):
    chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789-"
    for ch in chars:
        g = font.createChar(ord(ch))
        pat = PATTERNS.get(ch, [1,1,1,1,1,1,1]) # 未定義は全部点灯
        draw_segments(g, pat)
        g.width = 600
