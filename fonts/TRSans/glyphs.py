# 7セグ座標
SEGS = [
    (120,620,480,700), (80,380,160,620), (440,380,520,620),
    (120,340,480,420), (80,100,160,340), (440,100,520,340),
    (120,60,480,140)
]

def draw_segments(g, pattern):
    pen = g.glyphPen()
    for i,on in enumerate(pattern):
        if on:
            x1,y1,x2,y2 = SEGS[i]
            pen.moveTo((x1,y1)); pen.lineTo((x2,y1))
            pen.lineTo((x2,y2)); pen.lineTo((x1,y2)); pen.closePath()

